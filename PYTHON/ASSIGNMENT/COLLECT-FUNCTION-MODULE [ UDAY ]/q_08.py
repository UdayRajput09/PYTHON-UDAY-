# Write a Python program to convert a list of characters into a string.

# creating list of characters
l = [ 'U' , 'd' , 'a' , 'y' , 'R' , 'a' , 'j' , 'p' , 'u' , 't']

#joining characters to string in new list l1
l1 = ''.join(l)

